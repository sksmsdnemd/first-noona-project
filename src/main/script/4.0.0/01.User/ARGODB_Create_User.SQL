-- CREATE USER & GRANT
CREATE USER ARGO IDENTIFIED BY ARGO ;
GRANT CREATE SESSION TO ARGO;
GRANT CREATE TABLE, CREATE PROCEDURE, CREATE VIEW, CREATE TRIGGER TO ARGO;
GRANT CONNECT, DBA, RESOURCE TO ARGO;
GRANT SELECT ON DBA_FREE_SPACE TO ARGO;
GRANT SELECT ON DBA_TABLES TO ARGO;
GRANT SELECT ON DBA_DATA_FILES TO ARGO;
GRANT SELECT ON DBA_SEGMENTS TO ARGO;

-- PASSWORD UNLIMITED
ALTER PROFILE DEFAULT LIMIT PASSWORD_LIFE_TIME UNLIMITED;

-- PREVENT USER LOCK MODE
ALTER PROFILE DEFAULT LIMIT FAILED_LOGIN_ATTEMPTS UNLIMITED;

-- DB RECOVERY FILE SIZE
ALTER SYSTEM SET DB_RECOVERY_FILE_DEST_SIZE = 10240M

